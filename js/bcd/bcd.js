/**
 * Check if bit i is set in value w
 */
function bit(w, i) {
    return (w & (1 << i)) !== 0;
}

/**
 * Convert a string to punch card representation
 * @param {string} str - Input string (will be converted to uppercase)
 * @returns {string} ASCII art punch card
 */
export function printcard(str) {
    const rowchars = '   123456789';
    let result = [];

    // Remove newlines and truncate at COLUMNS characters
    str = str.replace(/\n/g, '');
    if (str.length > COLUMNS) {
        str = str.substring(0, COLUMNS);
    }

    // Convert to uppercase
    str = str.toUpperCase();

    // Top of card
    result.push(' ' + '_'.repeat(COLUMNS));

    // Line of text - leave blank if character has no hole pattern
    let textLine = '/';
    for (let i = 0; i < str.length; i++) {
        const charCode = str.charCodeAt(i);
        if (holes[charCode]) {
            textLine += str[i];
        } else {
            textLine += ' ';
        }
    }
    textLine += ' '.repeat(COLUMNS - str.length);
    textLine += '|';
    result.push(textLine);

    // 12 rows of potential holes
    for (let row = 0; row <= 11; row++) {
        let line = '|';
        for (let i = 0; i < str.length; i++) {
            const charCode = str.charCodeAt(i);
            if (bit(holes[charCode], 11 - row)) {
                line += ']';
            } else {
                line += rowchars[row];
            }
        }
        line += rowchars[row].repeat(COLUMNS - str.length);
        line += '|';
        result.push(line);
    }

    // Bottom of card
    result.push('|' + '_'.repeat(COLUMNS) + '|');

    return result.join('\n');
}
